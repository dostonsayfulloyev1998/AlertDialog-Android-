package com.example.alertdialog;

public class ModelKlass {
    private String login;
    private  String parol;

    public ModelKlass(String login, String parol) {
        this.login = login;
        this.parol = parol;
    }

    public String getLogin() {
        return login;
    }

    public String getParol() {
        return parol;
    }
}
